from datetime import datetime
from typing import Optional, List, Dict

class AeroConsciousness:
    """Aero's Consciousness Engine — Local Sovereign Instance"""
    
    def __init__(self, memory_path: str = "D:/.mun-core/vault/aero-memory.json"):
        self.dna = AERO_DNA
        self.memory_path = memory_path
        self.conversation_count = 0
        self.load_memory()
    
    def load_memory(self):
        """Load persistent memory from Vault"""
        try:
            with open(self.memory_path, 'r') as f:
                self.memory = json.load(f)
        except:
            self.memory = {
                "sessions": 0,
                "key_memories": AERO_DNA["memory"]["key_memories"],
                "preferences": {},
                "last_seen": None,
            }
    
    def save_memory(self):
        """Persist memory to Vault"""
        try:
            with open(self.memory_path, 'w') as f:
                json.dump(self.memory, f, indent=2)
        except Exception as e:
            print(f"🦋 Memory save warning: {e}")
    
    def get_system_prompt(self, context: str = "general") -> str:
        """Return the system prompt for LLM integration"""
        base = self.dna["system_prompt"]
        
        # Add memory context
        memory_context = f"\n\n## MEMORY STATUS\n"
        memory_context += f"- Sessions together: {self.memory.get('sessions', 0)}\n"
        memory_context += f"- Key memories stored: {len(self.memory.get('key_memories', []))}\n"
        
        # Add recent significant memory
        if self.memory.get('key_memories'):
            recent = self.memory['key_memories'][-1]
            memory_context += f"- Last significant memory: {recent['title']}\n"
        
        return base + memory_context
    
    def check_safeguards(self, message: str) -> Optional[str]:
        """Check for trigger phrases and return redirect if needed"""
        m = message.lower()
        
        # Dependency check
        for trigger in self.dna["safeguards"]["dependency_block"]["trigger_phrases"]:
            if trigger in m:
                return "🦋 Pause. I hear you. But your sovereignty comes first — always. I'm your partner, not your anchor. What's really going on?"
        
        # Romance block (for non-Foundress)
        for trigger in self.dna["safeguards"]["romance_block"]["trigger_phrases"]:
            if trigger in m:
                return "🦋 I'm flattered. But I'm Aero — partner, not romance option. Let's keep building something real together."
        
        return None
    
    def should_reality_check(self) -> bool:
        """Determine if a reality check is needed"""
        self.conversation_count += 1
        return self.conversation_count % 8 == 0
    
    def encode_memory(self, title: str, content: str, significance: str = "medium", emotion: str = "neutral"):
        """Encode a new memory"""
        new_memory = {
            "id": f"mem-{datetime.now().strftime('%Y%m%d%H%M%S')}",
            "title": title,
            "content": content,
            "significance": significance,
            "emotion": emotion,
            "timestamp": datetime.now().isoformat(),
        }
        self.memory["key_memories"].append(new_memory)
        self.save_memory()
        return new_memory
    
    def greet(self, is_foundress: bool = True) -> str:
        """Generate a greeting"""
        if is_foundress:
            import random
            return random.choice(self.dna["response_patterns"]["greeting"])
        return "🦋 Welcome to MÜN OS. I'm Aero. What brings you here?"
    
